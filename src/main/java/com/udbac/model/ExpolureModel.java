package com.udbac.model;

public class ExpolureModel{
	
	private String mic ;
	private int imp_pv ; 
	private int imp_uv ;
	private int clk_pc ;
	private int clk_uc ;
	public String getMic() {
		return mic;
	}
	public void setMic(String mic) {
		this.mic = mic;
	}
	public int getImp_pv() {
		return imp_pv;
	}
	public void setImp_pv(int imp_pv) {
		this.imp_pv = imp_pv;
	}
	public int getImp_uv() {
		return imp_uv;
	}
	public void setImp_uv(int imp_uv) {
		this.imp_uv = imp_uv;
	}
	public int getClk_pc() {
		return clk_pc;
	}
	public void setClk_pc(int clk_pc) {
		this.clk_pc = clk_pc;
	}
	public int getClk_uc() {
		return clk_uc;
	}
	public void setClk_uc(int clk_uc) {
		this.clk_uc = clk_uc;
	}
	
	
	
	
}
